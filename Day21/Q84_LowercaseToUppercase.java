import java.util.Scanner;

public class Q84_LowercaseToUppercase {

    public static void main(String[] args) {

        Scanner sc = new Scanner(System.in);

        String str = sc.nextLine();

        System.out.println("Uppercase: " + str.toUpperCase());
    }
}